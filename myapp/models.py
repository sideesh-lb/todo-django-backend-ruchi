class TodoList:
    def __init__(self, title, description, due_date, due_time, created_date, modified_date, status):
        self.title = title
        self.description = description
        self.due_date = due_date
        self.due_time = due_time
        self.created_date = created_date
        self.modified_date = modified_date
        self.status = status
